    private void selectData() {
        String kolom[] = {"Nama Petugas","No Resi","Nopol","Jenis Kendaraan","Tanggal Masuk","Tanggal Keluar","Harga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_titip";
        ResultSet rs = koneksidb.executeQuery(SQL);
        try{
            while(rs.next()){
                String nama = rs.getString(1);
                String noresi = rs.getString(2);
                String nopol = rs.getString(3);
                String jk = "";
                if("Motor".equals(rs.getString(4))) {
                    jk = "Motor";
                }
                else{
                    jk = "Mobil";
                }
                String tm = rs.getString(5);
                String tk = rs.getString(6);
                String data[] = {nama,noresi,nopol,jk,tm,tk};
                dtm.addRow(data);
            }
        }catch (SQLException ex){
            Logger.getLogger(main.class.getName()).log(Level.SEVERE,null,ex);
        }
        maintable.setModel(dtm);
    }
