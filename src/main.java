    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if("".equals(nam.getText()) || "".equals(res.getText()) || "".equals(pol.getText()) || 
                "".equals(mas.getText()) || "".equals(kel.getText())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
            } else {
            String JK = "";
                if (mot.isSelected()) {
                    JK = "Motor";
                } else {
                    JK = "Mobil";
                }
                String SQL = "INSERT INTO tb_titip (nama,noresi,nopol,jk,tm,tk) " + "VALUES('"+nam.getText()+"','"+res.getText()+"',"+"'"+pol.getText()+"','"+JK+"',"+"'"+mas.getText()+"','"+kel.getText()+"')";
            int status = koneksidb.execute(SQL);
            if (status ==1) {
                JOptionPane.showMessageDialog(this,"Data berhasil ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this,"Data gagal ditambahkan","Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_saveActionPerformed

    private void maintableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_maintableMouseClicked
        // TODO add your handling code here:
        int baris = maintable.getSelectedRow();
        if (baris != -1) {
            nam.setText(maintable.getValueAt(baris, 0).toString());
            res.setText(maintable.getValueAt(baris, 1).toString());
            pol.setText(maintable.getValueAt(baris, 2).toString());
            if("Motor".equals(maintable.getValueAt(baris, 3).toString())){
            mot.setSelected(true);
            }else {
            mob.setSelected(true);
            }
            mas.setText(maintable.getValueAt(baris, 4).toString());
            kel.setText(maintable.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_maintableMouseClicked

